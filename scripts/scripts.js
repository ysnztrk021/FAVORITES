function sortWebtoon(a, b) {
    if (a.webtoon.toLowerCase() < b.webtoon.toLowerCase()) {
        return -1;
    }
    if (a.webtoon.toLowerCase() > b.webtoon.toLowerCase()) {
        return 1;
    }
    return 0;
}

function sortSite(a, b) {
    if (a.constantly.toLowerCase() < b.constantly.toLowerCase()) {
        return -1;
    }
    if (a.constantly.toLowerCase() > b.constantly.toLowerCase()) {
        return 1;
    }
    return 0;
}